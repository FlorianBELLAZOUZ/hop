"use strict";

const assert = require( "assert" );

/*---------------------------------------------------------------------*/
/*    cast                                                             */
/*---------------------------------------------------------------------*/
function cast(x) {
   var r = 1, ttt;
   if((ttt=(x>>>16)) != 0) { x = ttt; r += 16; }
   if((ttt=(x>>8)) != 0) { x = ttt; r += 8; }
   return r;
}

assert.equal(cast({ val: (1 << 24) + 1}.val), 25);


