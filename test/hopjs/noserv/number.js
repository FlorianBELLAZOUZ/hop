var assert = require( "assert" );

/*---------------------------------------------------------------------*/
/*    associativity                                                    */
/*---------------------------------------------------------------------*/
assert.equal( 2+3*4, 2+(3*4) );
assert.equal( 2*3+4, (2*3)+4 );

/*---------------------------------------------------------------------*/
/*    integer limits                                                   */
/*---------------------------------------------------------------------*/
var x = 4294967295;

assert.equal( x, +x );
assert.equal( x, Math.ceil( x ) );

assert.ok( Math.floor( 2147483647.0 ) > 0 );

