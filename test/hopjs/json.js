var assert = require( "assert" );

/*---------------------------------------------------------------------*/
/*    stringify                                                        */
/*---------------------------------------------------------------------*/
function F() { return this; }
F.prototype = {FOO: 3, BAR: 4 };

var o = new F();
o.FOO = 5;
assert.strictEqual( JSON.stringify( o ), '{"FOO":5}' );

/*---------------------------------------------------------------------*/
/*    parse                                                            */
/*---------------------------------------------------------------------*/
assert.strictEqual( JSON.parse( "[1,2,3]" ).join( ","), "1,2,3" );
